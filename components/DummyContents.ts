export const getOrderStatusColor = (status: string) => {
  switch (status) {
    case "Delivered":
      return "text-[#3CD139]";
    case "In Transit":
      return "text-[#E3B231]";
    case "Cancelled":
      return "text-[red]";
    default:
      return "text-black";
  }
};

export const getOrderStatusBgColor = (status: string) => {
  switch (status) {
    case "Delivered":
      return "bg-[#ecfbec]";
    case "In Transit":
      return "bg-[#fdf8eb]";
    case "Cancelled":
      return "bg-red-100";
    default:
      return "bg-transparent";
  }
};

